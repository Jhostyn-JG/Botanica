for (const [key, product] of Object.entries(products)) {
    if (!product.name) {
        console.error(`Product ${key} is missing a name.`);
    }
}

function showContent(productKey) {
    const contentDiv = document.getElementById('content');
    const searchResultsDiv = document.getElementById('search-results');
    const product = products[productKey];
    if (product) {
        contentDiv.innerHTML = `
            <div class="product-details">
                <div class="contenedorImagenes">
                    <div class="product-image" style="background-image: url('${product.imageUrl}');"></div>
                    <div class="product-image" style="background-image: url('${product.imageUrl2}');"></div>
                </div>
                <div class="product-info">
                    <h2>${product.name}</h2>
                    <p>${product.description}</p>
                    <ul>
                        ${product.details.map(detail => `<li>${detail}</li>`).join('')}
                    </ul>
                </div>
            </div>
        `;
        searchResultsDiv.innerHTML = ''; 
    } else {
        contentDiv.textContent = 'No se encontró información del producto.';
    }
}



function searchProduct() {
    const searchInput = document.getElementById("search-input").value.toLowerCase().trim();
    const searchResultsDiv = document.getElementById('search-results');
    searchResultsDiv.innerHTML = '';
    const contentDiv = document.getElementById('content');
    const foundProducts = [];

    for (const [key, product] of Object.entries(products)) {
        // Verificar que product y product.name existen
        if (product && product.name && product.name.toLowerCase().includes(searchInput)) {
            foundProducts.push({ key, name: product.name });
        }
    }

    if (foundProducts.length > 0) {
        searchResultsDiv.innerHTML = `
            <ul>
                ${foundProducts.map(product => `<li onclick="showContent('${product.key}')">${product.name}</li>`).join('')}
            </ul>
        `;
    } else {
        searchResultsDiv.textContent = 'No se encontró información del producto.';
    }

    // Limpiar contenido principal
    contentDiv.innerHTML = '';
}
